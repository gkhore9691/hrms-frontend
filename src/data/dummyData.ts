// Generate 60 days of attendance for EMP001, EMP002, EMP003, EMP004, EMP005
function genAttRecords(): AttendanceRecord[] {
  const empIds = ["EMP001", "EMP002", "EMP003", "EMP004", "EMP005"];
  const statuses: AttendanceRecord["status"][] = ["Present", "Absent", "Half Day", "Late", "On Leave", "Holiday", "Weekend"];
  const out: AttendanceRecord[] = [];
  let id = 1;
  for (let d = -60; d <= 0; d++) {
    const date = new Date();
    date.setDate(date.getDate() + d);
    const dateStr = date.toISOString().slice(0, 10);
    const day = date.getDay();
    const isWeekend = day === 0 || day === 6;
    for (const empId of empIds) {
      const status = isWeekend
        ? "Weekend"
        : statuses[Math.floor(Math.random() * (statuses.length - 2))] ?? "Present";
      const isPresent = status === "Present" || status === "Late";
      out.push({
        id: `att${id++}`,
        employeeId: empId,
        date: dateStr,
        checkIn: isPresent ? "09:05" : null,
        checkOut: isPresent ? "18:10" : null,
        status,
        hoursWorked: isPresent ? 9.08 : 0,
        overtime: isPresent ? 0.08 : 0,
        shiftId: "s1",
      });
    }
  }
  return out;
}

